public class Type1 {
	public static void main(String[] args) {
		byte value1 = 10 ;
		short value2 = -10 ;
		int value3 = 100 ;
		long value4 = -100L ;
		System.out.println(value1);
		System.out.println(value2);
		System.out.println(value3);
		System.out.println(value4);
		System.out.println();
	}
}
