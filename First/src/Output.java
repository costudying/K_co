public class Output {
    public static void main(String[] args) {
       System.out.printf("%d\n" , 16);
       System.out.printf("%o\n" , 16);
       System.out.printf("%x\n" , 16);
       System.out.printf("%s\n" ,"문자열 출력");
       System.out.printf("%f\n" ,3.2582);
       System.out.printf("%4.2f\n" ,3.2582);
       System.out.printf("%5d, %.2f\n" , 16, 3.2582);
    }
}
